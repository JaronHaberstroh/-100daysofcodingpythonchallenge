# add() function
def add(n1, n2):
    return n1 + n2

# subtract() function
def subtract(n1, n2):
    return n1 - n2

# multiply() function
def multiply(n1, n2):
    return n1 * n2

# divide() function
def divide(n1, n2):
    return n1 / n2


operators = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide,
}


