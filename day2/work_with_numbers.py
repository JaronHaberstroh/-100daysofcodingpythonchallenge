# Add nums together with the + symbol

6 + 3

# Subtract nums with the - symbol

6 - 3

# Multiply nums with the * symbol

6 * 3

# Divide nums with the / symbol

6 / 3

# Exponents are represented with the ** symbol

6 ** 3

# Python follows PEMDAS from left to right

print(3 * 3 + 3 / 3 - 3) # print() 7 following PEMDAS

print(3 * ((3 + 3) / 3) - 3) # print() 3 following PEMDAS